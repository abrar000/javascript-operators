// Assignment operators

console.log( a = b); // a = 10 b = 10
console.log(a += b); // a = a + b => 20
console.log(a -= b); // a = a - b => 10
console.log(a *= b); // a = a * b => 100
console.log(a /= b); // a = a / b => 10
console.log(a % b); // a = a % b => 0

// Comparision operators
var x =5;
var y =10;
console.log(x == y); // equal
console.log(x != y); // not equal
console.log(x > y); // greater than
console.log(x < y); // less than
console.log (x >= y); // greater than or equal to
console.log (x <= y); // less than or equal to

